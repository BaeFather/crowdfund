<script type="text/javascript">
$('#sms_req_submit').click(function() {

	var text1 = $('#sms_receive_no1');
	var text2 = $('#sms_receive_no2');
	var text3 = $('#sms_receive_no3');

	//alert(String(text1.val()).length);

	if(text1.val()=='' || String(text1.val()).length < 3 ) {
		alert('문자메세지를 수신할 전화번호를 정확히 입력하여 주십시요.');
		text1.focus();
		return;
	}
	else if(text2.val()=='' || String(text2.val()).length < 3 ) {
		alert('문자메세지를 수신할 전화번호를 정확히 입력하여 주십시요.');
		text2.focus();
		return;
	}
	else if(text3.val()=='' || String(text3.val()).length < 4 ) {
		alert('문자메세지를 수신할 전화번호를 정확히 입력하여 주십시요.');
		text3.focus();
		return;
	}
	else if($('#receive_ok').is(':checked')==false) {
		alert('투자정보 안내 수신 및 휴대폰번호 등록에 동의하셔야 합니다.');
		$('#receive_ok').focus();
		return;
	}
	else {

		var text = String(text1.val()) + '' + String(text2.val()) + '' + String(text3.val());

		$.ajax({
			url : "/member/ajax_sms_request.php",
			type: "POST",
			data: {phone_no : text},
			success: function(data){
				if(data=="ERROR"){
					alert("시스템 에러입니다. 관리자에 문의해주세요.");
				}
				else if(data=="2"){
					alert("문자 수신이 가능한 모바일 번호가 아닙니다.\n문자메세지를 수신할 전화번호를 정확히 입력하여 주십시요.");
				}
				else {
					alert("정상 등록 되었습니다.");
					$.unblockUI();
				}
			},
			error: function () {
				alert("통신 에러입니다. 잠시 후 다시 시도하여 주십시요.");
			}
		});

	}
});
</script>

